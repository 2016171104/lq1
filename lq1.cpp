/*算术表达式*/
#include<stdio.h>
int main()
{
	printf("%.d\n", 8.0/5.0);
	printf("%d\n", 8.0/5.0);
	printf("%d\n", 8/5);
	printf("%.lf\n", 8.0/5.0);//四舍五入 
	printf("%.lf\n", 8/5);
	printf("%.1f\n", 8.0/5.0);
	printf("%.1f\n", 8/5);
	printf("%1f\n", 8.0/5.0);
	printf("%1f\n", 8/5);
	return 0;
	
 } 
